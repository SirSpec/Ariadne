    public GameWindow()
    {
        player = new Player
        (
            position: new Vector2(2.0f, 2.5f),
            direction: new Vector2(-1.0f, 0.0f),
            cameraPlane: new Vector2(0.0f, 0.66f)
        );

        rayCasting = new RayCasting(worldMap, screenWidth: 640, screenHeight: 320);
    }

    public char[][] Run(string key)
    {
        HandleInputs(key);
        return rayCasting.Cast(player);
    }

    private void HandleInputs(string key)
    {
        switch (key)
        {
            case "ArrowUp":
                player.MoveForward();
                break;
            case "ArrowDown":
                player.MoveBackward();
                break;
            case "ArrowRight":
                player.RotateRight();
                break;
            case "ArrowLeft":
                player.RotateLeft();
                break;
            default:
                break;
        }
    }
